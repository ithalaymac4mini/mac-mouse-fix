///
/// Let's you easily create objc dataclasses like this:
///     ```
///     MFDataClass3(MFDataClassBase, MFAddress, readwrite,  strong, nonnull,  NSString *,   city,
///                                              readonly,   strong, nullable, NSString *,   street,
///                                              readwrite,  assign,         , long,         zipcode)))
///     ```
///     -> This will create a dataclass `MFAddress` with 3 properties: `city`, `street`, and `zipcode`
///         (Use `MFDataClass3(...)` to create a dataclass with `3` properties, `MFDataClass4(...)` to create a dataclass with `4` properties, and so on...)
///
/// -> This will only work in an .m file. To export a dataClass inside an .h file, you need 2 definitions:
///     Put this in the .h file:        (it generates an objc`@interface`)
///         ```
///         MFDataClassInterface3(MFDataClassBase, MFAddress, readwrite,  strong, nonnull,  NSString *,   city,
///                                                           readonly,   strong, nullable, NSString *,   street,
///                                                           readwrite,  assign,         , long,         zipcode)))
///         ```
///     Put this in the .m file:        (it generates an objc `@implementation`)
///         ```
///         MFDataClassImplement3(MFDataClassBase, MFAddress, readwrite,  strong, nonnull,  NSString *,   city,
///                                                           readonly,   strong, nullable, NSString *,   street,
///                                                           readwrite,  assign,         , long,         zipcode)))
///         ```
///
///     -> Make sure to keep the implementation and the interface in sync! (You can simpy copy paste them)
///     -> `MFDataClassImpls.m` is intended as a dedicated place to put `MFDataClassImplementX()` macros that don't have a better place.
///
/// After you created your dataclass, you can create an instance of the dataclass using the autogenerated initializer:
///     ```
///     MFAddress *address = [[MFAddress alloc] initWith_city:@"Boston" street:@"1 Schroeder Plaza" zipcode:475839475];
///     ```
///
/// Getters and setters for all the properties will also be autogenerated
///     (That is not implemented by us, objc `@property` creates getters and setters automatically.)
///     (Setters won't be generated for `readonly` properties.)
///
/// The resulting dataclass will adhere to the `NSCopying`, and `NSSecureCoding` protocols. It will also respond properly to `- isEqual:` and `- hash`.
/// You can also get a list of all property names using `- allPropertyNames` and use those names as keys for Key-Value-Coding methods such as `- valueForKey:` and `- setValue:ForKey:`
///     -> So you can kinda treat `MFDataClass` instances like a dictionary and dynamically access everything! You can also get an actual dictionary representing the object-graph using the `- asPlistWithRequireSecureCoding:` method.
///
/// Under DEBUG builds, immediately after the program loads (Inside `+ load`), the MFDataClass will do pretty extensive validation of your definition to catch any pitfalls.
///     We validate things such as whether all the properties support NSCopying (which is necessary for copying of the dataclass to work) or whether the dataclass actually inherits from `MFDataClassBase`.
///     When the validation succeeds, all the behaviors and methods described above should be guaranteed to work correctly.
///     (Hope this validation doesn't become annoying or restrictive at some point.)
///
/// Details:
///     - When creating a new `MFDataClass`, you have to specifiy the superclass. (This is all validated in `+ load`, as of Oct 2024)
///         - Default to using `MFDataClassBase` as the superclass.
///         - You can also create a dataclass with **0 properties** and use that as the superclass for other dataclasses. Dataclasses with 0 properties don't really do anything, except serve as superclasses to group other dataclasses together in a way that the type system understands.
///         - Dataclasses with 1 or more properties shouldn't be used as superclasses of other dataclasses, since that will break the initializers. Nest dataclasses in other dataclasses instead of trying to inherit properties from another dataclass.
///     - Restrictions on property types: (This is all validated in `+ load`, as of Oct 2024)
///         For copying, encoding, hashing, and equality-checking of the `MFDataClass` to work properly, all of its properties need to implement `NSCopying`, `NSSecureCoding`, `- hash` and `- isEqual`.
///         Non-Object types should also work for the most part, since they can be boxed in an `NSValue` object which implements all the aforementioned protocols and methods out-of-the-box
///             However, Non-Object types which are not *auto-boxed* in an `NSValue` by the Key-Value-Coding APIs will *not* work properly with `MFDataClass`.
///                 (E.g. c unions won't work from what I heard, but any custom structs and c-number-types should work according to the docs).
///             Docs: (KeyValueCoding - Representing Non-Object Values) https://developer.apple.com/library/archive/documentation/Cocoa/Conceptual/KeyValueCoding/DataTypes.html#//apple_ref/doc/uid/20002171-BAJEAIEE)
///    - Property attributes:
///         1. `atomic` cannot be specified. (We always set it to `nonatomic` automatically - We do this because I don't ever want to use atomic, and if left unspecified, objc defaults to atomic.)
///         All other property attributes can be specified. Here are some guidelines:
///         2. `strong` or `assign` should be specified.
///             Always use`assign` for non-object types. Generally use `strong` for objects. Other possible values for objects are `weak` and `copy` (and `retain` which is the same as `strong` under ARC.)
///             These values specify the so called "Setter Semantics".
///         3. `readonly` or `readwrite` should be specified. (Can also be left blank to default to readwrite.)
///         4. `nullable` or `nonnull` needs to be specified for objects and pointers, but needs to be left blank for anything else.
///             The Objective-C runtime cannot tell us whether a property is `nullable/nonnull` so we need to always specify nullability in the `MFDataClassX()` macros for objects and pointers. The macros then store the information in a method for runtime access. This way we can access all property attributes at runtime.
///             Discussion: Normally, I find it nicer to specify nullability as part of the type system (using `_Nonnull`/`_Nullable`) instead of as property attributes (using `nullable`/`nonnull`) - but for the sake of manually storing `nullable`/`nonnull` for runtime access, this works better.
///         -> So in conclusion: Generally, specify readonly/readwrite, strong/assign, nullable/nonnull/blank.
///         More info on `Setter Semantics` and other objc `property attributes` in the docs: https://developer.apple.com/library/archive/documentation/Cocoa/Conceptual/ObjectiveC/Chapters/ocProperties.html
///    - Thread safety: These dataclasses are not thread safe. Use locks or dispatch queues to prevent concurrent access while modifying the properties of a dataclass instance.
///    - The implemenation macros (`MFDataClassImplementX(...)`) actually don't use many of their arguments, such as the superclass and the setterSemantics. They have these extra arguments so that the arguments match exactly with corresponding interface-macros (`MFDataClassInterfaceX(...)`)  which allows for easy copy-pasting.
///     - The  -[hash] is just the number of properties. This allows MFDataClass instances to be stored in objc collections. -[NSDictionary hash] behaves the same way.
///         To get a real **cryptographic hash** you should serialize the MFDataClass instance to data and then use a real hash-function on that. (Doing that inside GetLicenseState.swift as of [Jan 2025])
///
///  Implementation Sidenotes:
///     - Is this overcomplicated?
///         If we didn't autogenerate initializers, then implementing the macros could be 30 lines of code instead of 1000 - because we wouldn't have to write a separate macro for each possible number of properties.
///         Why did we still do it?
///         - Initializers bring nice compile-time-checks: When you add a new property to an `MFDataClass()` you'll get a compiler error if you forget to initialize the new property somewhere, which wouldn't be possible without initializers I think.
///         - Initializers let us have properties that are definitely never `null`, which lets us import those properties as non-optionals into Swift - that's nicer to deal with.
///         - These many lines of macros are actually quite simple and repetitive and we can just let an LLM generate them.
///             -> Update [Feb 2025] we've since added the +rawNullabilityAndTypeOfProperty: methods which also require separate macros for each possible number of arguments, and which is necessary for some automatic decoding validations.
///     - Like half of the code in `MFDataClass.m` is to implement `NSSecureCoding`
///         - After thinking about it I think it isn't really that useful for us... but now we already wrote the code.
///         - Read more about our thoughts on NSSecureCoding in `MFCoding.m`
///
/// Meta:
/// - Out of NSArray and MFDataClass we should be able to build any interesting arrangement of data. They would serve as an object-based alterative to array (> NSArray) and struct (> MFDataClass) from C.
/// - We should consider replacing the remapsDict (Which is a big nested `NSDictionary`that represents the mouse-button-to-action mappings inside the Mac Mouse Fix Helper process) with a nested set of `MFDataClass` instances. That would make it less scary to work with. (Although honestly it's been surprisingly fine, even though the compiler doesn't know the structure of this big nested dict at all.)
///     (Or maybe a nested struct definition in swift would be even nicer, but not sure if fast and objc compatible. Nested class definition in Swift might also be nice but afaik you can't autogenerate initializers and NSCoding/NSCopying/-isEqual: support like you can with Swift structs and with MFDataClass)

